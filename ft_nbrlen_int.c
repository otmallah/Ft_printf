#include "ft_printf.h"

int	ft_count_int(int numb)
{
	int	t;
	int	i;

	i = 0;
	t = numb;
	if (t < 0)
	{
		t *= -1;
		i++;
	}
	if (t == 0)
		i = 1;
	while (t != 0)
	{
		t /= 10;
		i++;
	}
	return (i);
}
